    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       int linha = Jtabela.getSelectedRow();
        if(linha==-1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }
        
        else if(JOptionPane.showConfirmDialog(null, "Deseja realmente excluir essa Linha?", "Excluir", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
            
            usuario = dao.pesquisarUsuarioID((int) Jtabela.getValueAt(linha, 0));
            dao.deletar(usuario);
            exibeTela();
            JOptionPane.showMessageDialog(null,"Usuario excluido com Sucesso!");
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         int linha = Jtabela.getSelectedRow();
        if(linha==-1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }
        
        else{
            
            usuario = dao.pesquisarUsuarioID((int) Jtabela.getValueAt(linha, 0));
            
            CadastroUsuario  cdu = new CadastroUsuario();
            cdu.usuario = usuario;
            cdu.atualizarCamposConsulta();
            cdu.setVisible(true);
            dispose();
           
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

