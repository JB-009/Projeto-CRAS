    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     
        int linha = Jtable.getSelectedRow();
        if(linha==-1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }
        
        else if(JOptionPane.showConfirmDialog(null, "Deseja realmente excluir essa Linha?", "Excluir", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
            
            fp = fpdao.pesquisarCasoID((int) Jtable.getValueAt(linha, 0));
            fpdao.deletar(fp);
            exibeTela();
            JOptionPane.showMessageDialog(null,"Usuario excluido com Sucesso!");
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linha = Jtable.getSelectedRow();
        if(linha==-1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }
        
        else{
            
            fp = fpdao.pesquisarCasoID((int) Jtable.getValueAt(linha, 0));
            
          FormGeral fg = new FormGeral();
          fg.fp = fp;
            fg.atualizarCamposConsulta();
            fg.setVisible(true);
             dispose();
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

